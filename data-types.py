# In programming, data type is an important concept.

# Variables can store data of different types, and different types can do different things.

# Python has the following data types built-in by default, in these categories:

# Text Type:	str
# Numeric Types:	int, float, complex
# Sequence Types:	list, tuple, range
# Mapping Type:	dict
# Set Types:	set, frozenset
# Boolean Type:	bool
# Binary Types:	bytes, bytearray, memoryview

# Getting the Data Type
# You can get the data type of any object by using the type() function:

# Example of str data type

# x = "Hello World"
# print(type(x))

# Example of int data type

# a = 20
# print(type(a))

# Example of float data type

# b = 20.5
# print(type(b))

# Example of complex data type
# c = 1j
# print(type(c))

# Example of list data type 
# d = ["apple", "banana", "cherry"]
# print(d[1])
# print(type(d))

# Example of tuple data type
# e = ("apple", "banana", "cherry")
# print(e[1])
# print(type(e))

# Example of range data type
# f = range(6)
# print(type(f))

# Example of dict data type
# g = {"name" : "John", "age" : 36}

# print(g["name"])
# print(type(g))

# Example of set data type

# h = {"apple", "banana", "cherry"}
# print(h)
# print(type(h))

# Example of frozenset data type
i = frozenset({"apple", "banana", "cherry"})
print(type(i))

# Example of bool data type
j = True
print(type(j))

# # Example of bytes data type
k = b"Hello"
print(type(k))

# Example of bytearray data type
# l = bytearray(5)
# print(type(l))

# Example of memoryview data type
# m = memoryview(bytes(5))
# print(type(m))

